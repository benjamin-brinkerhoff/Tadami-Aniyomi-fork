 * 4. Р В Р’В Р РҺРЎСҹР В Р РӢР В РІРӮСҷР В Р’В Р РҺРІРӮСһР В Р’В Р СһРІРӮВҳР В Р’В Р РҺРІРӮСһР В Р’В Р вҖҷР’В»Р В Р’В Р вҖҷР’В¶Р В Р’В Р вҖҷР’В°Р В Р РӢР РҶР вҖҡРЎв„ўР В Р РӢР В Р вҖ° Р В Р’В Р РҺРІРӮвҖқР В Р’В Р РҺРІРӮСһР В Р’В Р РҺРІРӮСңР В Р’В Р вҖҷР’В° Р В Р’В Р вҖҷР’ВөР В Р РӢР В РЎвҖңР В Р РӢР РҶР вҖҡРЎв„ўР В Р РӢР В Р вҖ° Р В Р’В Р вҖҷР’В°Р В Р’В Р РҺРІРӮСңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳР В Р’В Р В РІРӮВ Р В Р’В Р В РІРӮВҰР В Р’В Р РҺРІРӮСһР В Р РӢР В РЎвҖңР В Р РӢР РҶР вҖҡРЎв„ўР В Р РӢР В Р вҖ° Р В Р’В Р В РІРӮВ  Р В Р’В Р РҺРІРӮСңР В Р’В Р вҖҷР’В°Р В Р’В Р вҖҷР’В¶Р В Р’В Р СһРІРӮВҳР В Р РӢР РҶР вҖҡРІвҖһвҖ“Р В Р’В Р РҶРІРӮСӣРІРӮвҖң Р В Р’В Р СһРІРӮВҳР В Р’В Р вҖҷР’ВөР В Р’В Р В РІРӮВҰР В Р РӢР В Р вҖ°
 * 5. Р В Р’В Р РҺРЎСҹР В Р РӢР В РІРӮСҷР В Р’В Р вҖҷР’ВөР В Р РӢР В РІРӮСҷР В Р’В Р В РІРӮВ Р В Р’В Р вҖҷР’В°Р В Р РӢР РҶР вҖҡРЎв„ўР В Р РӢР В Р вҖ° Р В Р’В Р вҖҷР’ВөР В Р РӢР В РЎвҖңР В Р’В Р вҖҷР’В»Р В Р’В Р РҺРІРӮВҳ Р В Р’В Р СһРІРӮВҳР В Р’В Р В РІРӮВҰР В Р РӢР В Р РҸ Р В Р’В Р вҖҷР’ВұР В Р’В Р вҖҷР’ВөР В Р’В Р вҖҷР’В· Р В Р’В Р вҖҷР’В°Р В Р’В Р РҺРІРӮСңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳР В Р’В Р В РІРӮВ Р В Р’В Р В РІРӮВҰР В Р’В Р РҺРІРӮСһР В Р РӢР В РЎвҖңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳ
 *
 * @param database Р В Р’В Р РҶР вҖҡР’ВҳР В Р’В Р вҖҷР’В°Р В Р’В Р вҖҷР’В·Р В Р’В Р вҖҷР’В° Р В Р’В Р СһРІРӮВҳР В Р’В Р вҖҷР’В°Р В Р’В Р В РІРӮВҰР В Р’В Р В РІРӮВҰР В Р РӢР РҶР вҖҡРІвҖһвҖ“Р В Р РӢР РҶР вҖҡР’ВҰ Р В Р’В Р СһРІРӮВҳР В Р’В Р РҺРІРӮСһР В Р РӢР В РЎвҖңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳР В Р’В Р вҖҷР’В¶Р В Р’В Р вҖҷР’ВөР В Р’В Р В РІРӮВҰР В Р’В Р РҺРІРӮВҳР В Р’В Р РҶРІРӮСӣРІРӮвҖң Р В Р’В Р СһРІРӮВҳР В Р’В Р вҖҷР’В»Р В Р РӢР В Р РҸ Р В Р’В Р вҖҷР’В»Р В Р’В Р РҺРІРӮСһР В Р’В Р РҺРІРӮвҖңР В Р’В Р РҺРІРӮВҳР В Р РӢР В РІРӮСҷР В Р’В Р РҺРІРӮСһР В Р’В Р В РІРӮВ Р В Р’В Р вҖҷР’В°Р В Р’В Р В РІРӮВҰР В Р’В Р РҺРІРӮВҳР В Р РӢР В Р РҸ Р В Р’В Р вҖҷР’В°Р В Р’В Р РҺРІРӮСңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳР В Р’В Р В РІРӮВ Р В Р’В Р В РІРӮВҰР В Р’В Р РҺРІРӮСһР В Р РӢР В РЎвҖңР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р РҺРІРӮВҳ
 *
 * @see AchievementType.STREAK
 */

class StreakAchievementChecker(
    private val database: AchievementsDatabase,
) {

    companion object {
        /** Р В Р’В Р РҺРЎв„ўР В Р’В Р вҖҷР’В°Р В Р’В Р РҺРІРӮСңР В Р РӢР В РЎвҖңР В Р’В Р РҺРІРӮВҳР В Р’В Р РҺР’ВҳР В Р’В Р вҖҷР’В°Р В Р’В Р вҖҷР’В»Р В Р РӢР В Р вҖ°Р В Р’В Р В РІРӮВҰР В Р’В Р вҖҷР’В°Р В Р РӢР В Р РҸ Р В Р РӢР В РЎвҖңР В Р’В Р вҖҷР’ВөР В Р РӢР В РІРӮСҷР В Р’В Р РҺРІРӮВҳР В Р РӢР В Р РҸ Р В Р’В Р СһРІРӮВҳР В Р’В Р вҖҷР’В»Р В Р РӢР В Р РҸ Р В Р’В Р РҺРІРӮвҖқР В Р РӢР В РІРӮСҷР В Р’В Р РҺРІРӮСһР В Р’В Р В РІРӮВ Р В Р’В Р вҖҷР’ВөР В Р РӢР В РІРӮСҷР В Р’В Р РҺРІРӮСңР В Р’В Р РҺРІРӮВҳ (Р В Р’В Р РҺРІРӮвҖқР В Р РӢР В РІРӮСҷР В Р’В Р вҖҷР’ВөР В Р’В Р СһРІРӮВҳР В Р’В Р РҺРІРӮСһР В Р РӢР РҶР вҖҡРЎв„ўР В Р’В Р В РІРӮВ Р В Р РӢР В РІРӮСҷР В Р’В Р вҖҷР’В°Р В Р РӢР РҶР вҖҡР’В°Р В Р’В Р вҖҷР’В°Р В Р’В Р вҖҷР’ВөР В Р РӢР РҶР вҖҡРЎв„ў Р В Р’В Р вҖҷР’ВұР В Р’В Р вҖҷР’ВөР В Р РӢР В РЎвҖңР В Р’В Р РҺРІРӮСңР В Р’В Р РҺРІРӮСһР В Р’В Р В РІРӮВҰР В Р’В Р вҖҷР’ВөР В Р РӢР РҶР вҖҡР РӢР В Р’В Р В РІРӮВҰР В Р РӢР РҶР вҖҡРІвҖһвҖ“Р В Р’В Р вҖҷР’Вө Р В Р РӢР РҶР вҖҡР’В Р В Р’В Р РҺРІРӮВҳР В Р’В Р РҺРІРӮСңР В Р’В Р вҖҷР’В»Р В Р РӢР РҶР вҖҡРІвҖһвҖ“) */
        private const val MAX_STREAK_DAYS = 365

        private val DATE_FORMATTER = DateTimeFormatter.ISO_LOCAL_DATE
    }

    /**
     * Calculate the current streak of consecutive days with activity.
     * Does not break streak if there's no activity yet today.
     */
    suspend fun getCurrentStreak(): Int {
        var streak = 0
        var checkDate = LocalDate.now()
        var checkedToday = false

        // Check up to MAX_STREAK_DAYS back
        repeat(MAX_STREAK_DAYS) {
            val activity = getActivityForDate(checkDate)

            when {
                // First iteration (today): no activity yet is OK, check yesterday
                !checkedToday && activity == null -> {
                    checkedToday = true
                    checkDate = checkDate.minusDays(1)
                    return@repeat
                }
                // First iteration (today): has activity, count it and continue
                !checkedToday && hasActivity(activity) -> {
                    checkedToday = true
                    streak++
                    checkDate = checkDate.minusDays(1)
                    return@repeat
                }
                // First iteration (today): no activity log at all, check yesterday
                !checkedToday -> {
                    checkedToday = true
                    checkDate = checkDate.minusDays(1)
                    return@repeat
                }
                // Subsequent iterations: need activity to continue streak
                hasActivity(activity) -> {
                    streak++
                    checkDate = checkDate.minusDays(1)
                    return@repeat
                }
                // No activity on this day, streak broken
                else -> return streak
            }
        }

        return streak
    }

    /**
     * Record that a chapter was read today.
     */
    suspend fun logChapterRead() {
        val today = LocalDate.now().format(DATE_FORMATTER)
        val now = System.currentTimeMillis()

        database.activityLogQueries.incrementChapters(
            date = today,
            level = 1, // Will be calculated by the query based on count
            count = 1,
            last_updated = now,
        )
    }

    /**
     * Record that an episode was watched today.
     */
    suspend fun logEpisodeWatched() {
        val today = LocalDate.now().format(DATE_FORMATTER)
        val now = System.currentTimeMillis()

        database.activityLogQueries.incrementEpisodes(
            date = today,
            level = 1, // Will be calculated by the query based on count
            count = 1,
            last_updated = now,
        )
    }

    /**
     * Get the activity record for a specific date.
     */
    private suspend fun getActivityForDate(date: LocalDate): ActivityLog? {
        val dateStr = date.format(DATE_FORMATTER)
        val record: Activity_log? = database.activityLogQueries
            .getActivityForDate(dateStr)
            .executeAsOneOrNull()

        return if (record != null) {
            ActivityLog(
                date = dateStr,
                chapterCount = record.chapters_read,
                episodeCount = record.episodes_watched,
                lastUpdated = record.last_updated,
            )
        } else {
            null
        }
    }

    /**
     * Check if an activity log contains any activity.
     */
    private fun hasActivity(activity: ActivityLog?): Boolean {
        return activity != null && (activity.chapterCount > 0 || activity.episodeCount > 0)
    }

    /**
     * Data class representing an activity log entry.
     */
    private data class ActivityLog(
        val date: String,
        val chapterCount: Long,
        val episodeCount: Long,
        val lastUpdated: Long,
    )
}
