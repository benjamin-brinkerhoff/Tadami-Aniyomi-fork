package eu.kanade.domain.easteregg.aurora

/**
 * Канонические кодировки «не-текстовых» каналов ввода ответа.
 *
 * Идея безопасности: код ничего не знает о правильном ответе.
 * Он лишь превращает ЛЮБОЕ действие пользователя в каноническую фразу
 * и отдаёт её в [AuroraQuest.offer]. Какое именно действие нужно — знает
 * только зашифрованный текст загадки. Поэтому все каналы могут
 * слушать ПОСТОЯННО — анализ кода не выдаёт ни канал ступени, ни ответ.
 *
 * Форматы фраз (их же используй в scenario.json как answer):
 *  - поиск:            просто текст запроса, например "час волка"
 *  - сигиль (жест):   "sigil:2-5-8" — путь по ячейкам 3x3 (нумерация 1..9,
 *                       слева направо, сверху вниз)
 *  - ритуал (действие в определённый час): "ритуал:закладка:03"
 *  - именование (имя категории и т.п.): "категория:северный путь"
 *
 * ВАЖНО: не менять форматы после генерации хранилища — фраза должна
 * бит в бит совпадать с тем, что было зашито в scenario.json.
 */
object AuroraChannels {

    /** Глобальный поиск: фраза передаётся как есть. */
    fun search(query: String): String = query

    /**
     * Сигиль: путь по сетке 3x3 (как графический ключ Android).
     * Ячейки нумеруются 1..9: верхний ряд 1-2-3, средний 4-5-6, нижний 7-8-9.
     * Возвращает null для слишком коротких/некорректных путей.
     */
    fun sigil(cells: List<Int>): String? {
        if (cells.size !in 3..9) return null
        if (cells.any { it !in 1..9 }) return null
        return "sigil:" + cells.joinToString("-")
    }

    /**
     * Ритуал: любое именованное действие, совершённое в определённый час.
     * Пример: добавление в избранное в 3 часа ночи -> "ритуал:избранное:03".
     */
    fun ritual(action: String, hourOfDay: Int): String =
        "ритуал:" + action.trim().lowercase() + ":" + "%02d".format(hourOfDay)

    /**
     * Именование: пользователь даёт чему-то имя (категория, бэкап и т.п.).
     * Пример: создание категории с именем -> named("категория", name).
     */
    fun named(kind: String, value: String): String =
        kind.trim().lowercase() + ":" + value.trim().lowercase()
}
